    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // Crear Usuario
        String nombre, user,pass;
        int edad;
        int resp=0;
        
        try {
            nombre = tf_nombre.getText();
            user=tf_user.getText();
            pass=pf_pass.getText();
            if (tf_edad.getText().equals("")) {
                edad=0;
            }else{
              edad=Integer.parseInt(tf_edad.getText());  
            }
            
            if (nombre.equals("")) {
                resp=1;
            }
            if (user.equals("")) {
                resp=1;
            }
            if (pass.equals("")) {
                resp=1;
            }
            if (tf_edad.getText().equals("")||edad==0) {
                resp=1;
            }
            
                
            for (int i = 0; i < usuarios.size(); i++) {
                
                if(usuarios.get(i).getNombre().equalsIgnoreCase(nombre)){
                    resp=2;
                    
                }
            }
            
            //guardar
            if (resp==0) {
                
            
            usuarios.add(new Usuario(nombre, edad, user, pass));
            
            JOptionPane.showMessageDialog(JD_usuarios, "Cuenta Creada Exitosamente");
                
            //resetear controles
            tf_nombre.setText("");
            tf_user.setText("");
            tf_edad.setText("");
            pf_pass.setText("");
            }
            if (resp==2) {
                JOptionPane.showMessageDialog(JD_usuarios, "Ese nombre ya fue ingresado, intente con otro");
            }
            if(resp==1){
                JOptionPane.showMessageDialog(JD_usuarios, "Olvido llenar algun campo!");
            }
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(JD_usuarios, "Ocurrio un error y no se creo una cuenta");
            
        }
        
    }//GEN-LAST:event_jButton7ActionPerformed


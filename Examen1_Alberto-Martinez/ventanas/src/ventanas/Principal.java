    private void jb_GuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_GuardarMouseClicked
        int codigo;
        String Nombre, sexo, direccion, idioma;
        Date fecha;
        Color c;
        String h1 = "", h2 = "", h3 = "", h4 = "", h5 = "";
        try {
            codigo = Integer.parseInt(tf_Codigo.getText());
            Nombre = tf_Nombre.getText();
            if(rb_M.isSelected()){
                sexo = "M";
            }else{
                sexo = "F";
            }
            fecha=jd_Fecha.getDate();
            c=jb_Color.getBackground();
            direccion=ta_Direccion.getText();
            idioma=cb_idioma.getSelectedItem().toString();
            if (cb_football.isSelected()) {
                h1="Football";
            }
            if (cb_TV.isSelected()) {
                h2="TV";
            }
            if (cb_Musica.isSelected()) {
                h3="Musica";
            }if (cb_Correr.isSelected()) {
                h4="Correr";
            }if (cb_Bailar.isSelected()) {
                h5="Bailar";
            }if (pb_Amigos.getValue()<10) {
                //guardar
                Contactos x=new Contactos(codigo, Nombre, sexo, fecha, c, direccion, idioma, h1, h2, h3, h4, h5);
                lista.add(x);

                //agregar ComboBox
                DefaultComboBoxModel modelo= (DefaultComboBoxModel)cb_amigos.getModel();
                modelo.addElement(x);

                //modificar en un cb
                //((Contactos)modelo.getSelectedItem()).setColor(Color.blue);

                //eliminar
                //modelo.removeElementAt(cb_amigos.getSelectedIndex());

                cb_amigos.setModel(modelo);

                pb_Amigos.setValue(lista.size());
                pb_Amigos.setString(
                    Integer.toString(pb_Amigos.getValue()*100/10)+"%");

                //resetear controles
                tf_Codigo.setText("");
                tf_Nombre.setText("");
                rb_M.setSelected(true);
                rb_F.setSelected(false);
                jd_Fecha.setDate(new Date());
                jb_Color.setBackground(Color.red);
                ta_Direccion.setText("");
                cb_idioma.setSelectedIndex(0);
                cb_Bailar.setSelected(false);
                cb_Correr.setSelected(false);
                cb_TV.setSelected(false);
                cb_football.setSelected(false);
                cb_Musica.setSelected(false);

            }else{
                JOptionPane.showMessageDialog(this, "Ya no hay espacio");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Ocurrio un error y no se guardaron los datos");

        }
    }//GEN-LAST:event_jb_GuardarMouseClicked

    private void cb_amigosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cb_amigosItemStateChanged
        if (evt.getStateChange()==2){
            Contactos s=(Contactos)cb_amigos.getSelectedItem();
            Object[] newrow={
                s.getCodigo(),
                s.getNombre(),
                ((new Date()).getTime()-s.getFecha_Nacimiento().getTime())/(1000*60*60*24),/*dias*/s.getFecha_Nacimiento()
            };
            DefaultTableModel modelo=(DefaultTableModel)tabla1.getModel();
            modelo.addRow(newrow);
            tabla1.setModel(modelo);
        }
    }//GEN-LAST:event_cb_amigosItemStateChanged

